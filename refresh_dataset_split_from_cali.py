"""Rewrite traning data/dataset_split.json from all *cali.txt (no backup/)."""
import json
from collections import defaultdict
from pathlib import Path

from training_data_root import get_training_data_root

root = Path(get_training_data_root())
cali_files = sorted(p for p in root.rglob("*cali.txt") if "backup" not in p.parts)
by_folder = defaultdict(set)
for cali in cali_files:
    stem = cali.name[: -len("cali.txt")]
    vid = cali.parent / f"{stem}.mp4"
    fk = cali.parent.relative_to(root).as_posix()
    by_folder[fk].add(vid.name)

train, val = [], []
for fk in sorted(by_folder.keys()):
    vids = sorted(by_folder[fk])
    if not vids:
        continue
    val.append(f"{fk}/{vids[0]}".replace("\\", "/"))
    for v in vids[1:]:
        train.append(f"{fk}/{v}".replace("\\", "/"))

train.sort()
val.sort()
out = {
    "note": "Regenerated by refresh_dataset_split_from_cali.py: first sorted .mp4 per leaf folder = val.",
    "train": train,
    "val": val,
}
path = root / "dataset_split.json"
path.write_text(json.dumps(out, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
print(f"Wrote {path}  train={len(train)}  val={len(val)}  total={len(train)+len(val)}")
